    public void deleteMonHoc(MONHOC mh) {
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn xóa môn học này?", "Xác nhận xóa môn học", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if (confirm == JOptionPane.YES_OPTION) {
            String query = "EXEC QLDSV_TC.DBO.SP_XOAMONHOC '" + mh.getMamh() + "'";
            Statement stmt = this.variables.getDatabase().getStatement();
            try {
                stmt.execute(query);
                this.variables.getDatabase().getMonHocManager().getListMONHOC().clear();
                this.variables.getDatabase().loadListMonHoc();
                this.putDataIntoTable();
                JOptionPane.showMessageDialog(this, "Xóa môn học thành công", "Thành công", JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException ex) {
                Logger.getLogger(pn_MonHoc.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Không thể xóa môn học này", "Thất bại", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    
    public void searchMonHoc() {
        String maMH = txt_timKiem.getText();
        String query = "EXEC QLDSV_TC.DBO.SP_TIMMONHOC '" + maMH + "'";
        Statement stmt = this.variables.getDatabase().getStatement();
        try {
            ResultSet rs = stmt.executeQuery(query);
            this.variables.getDatabase().getMonHocManager().getListMONHOC().clear();
            while(rs.next()) {
                MONHOC mh = new MONHOC();
                mh.setMamh(rs.getString("MAMH"));
                mh.setTenmh(rs.getString("TENMH"));
                mh.setStlt(rs.getInt("SOTIET_LT"));
                mh.setStth(rs.getInt("SOTIET_TH"));
                this.variables.getDatabase().getMonHocManager().insertMONHOC(mh);
            }
            putDataIntoTable();
        } catch (SQLException ex) {
            Logger.getLogger(pn_MonHoc.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void putDataIntoTable() {
        DefaultTableModel subjectModel = (DefaultTableModel) tbl_MonHoc.getModel();
        subjectModel.setRowCount(0);
        List<MONHOC> myList = variables.getDatabase().getMonHocManager().getListMONHOC();
        for (MONHOC item: myList){
            Object[] obj = new Object[]{
                item.getMamh(),
                item.getTenmh(),
                item.getStlt(),
                item.getStth(),
            };
            subjectModel.addRow(obj);
        }
        sortTable();
    }
    
    public void putDataIntoForm(MONHOC mh) {
        txt_maMonHoc.setText(mh.getMamh());
        txt_tenMonHoc.setText(mh.getTenmh());
        txt_soTietLyThuyet.setText(String.valueOf(mh.getStlt()));
        txt_soTietThucHanh.setText(String.valueOf(mh.getStth()));
    }
    
    private void sortTable() {
        DefaultTableModel teacherModel = (DefaultTableModel) tbl_MonHoc.getModel();
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(teacherModel);
        tbl_MonHoc.setRowSorter(sorter);
    }
    
    public void clearAllDataFromTable() {
        DefaultTableModel subjectModel = (DefaultTableModel) tbl_MonHoc.getModel();
        subjectModel.setRowCount(0);
    }
    
