    public void insertMonHoc() {
        String maMH = txt_maMonHoc.getText();
        String tenMH = txt_tenMonHoc.getText();
        String STLT = txt_soTietLyThuyet.getText();
        String STTH = txt_soTietThucHanh.getText();
        
        String query = "EXEC QLDSV_TC.DBO.SP_THEMMH '" + maMH + "', N'" + tenMH + "', '" + STLT + "', '" + STTH + "'";
        Statement stmt = this.variables.getDatabase().getStatement();
        try {
            stmt.execute(query);
            this.variables.getDatabase().getMonHocManager().getListMONHOC().clear();
            this.variables.getDatabase().loadListMonHoc();
            this.putDataIntoTable();
            JOptionPane.showMessageDialog(this, "Thêm môn học thành công", "Thành công", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(pn_MonHoc.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Thêm môn học thất bại", "Thất bại", JOptionPane.ERROR_MESSAGE);
        }
    }
    
