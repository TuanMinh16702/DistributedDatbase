    public void putDataIntoTable() {
        DefaultTableModel classModel = (DefaultTableModel) tbl_Lop.getModel();
        classModel.setRowCount(0);
        List<LOP> myList = variables.getDatabase().getLopManager().getListLOP();
        for (LOP item: myList){
            System.out.println(item.getMaLop());
            Object[] obj = new Object[]{
                item.getMaLop(),
                item.getTenLop(),
                item.getKhoaHoc(),
                item.getMaKhoa(),
            };
            classModel.addRow(obj);
        }
        sortTable();
    }
    
