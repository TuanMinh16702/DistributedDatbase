    public void deleteLopHoc() {
        int confirm = JOptionPane.showConfirmDialog(this, "Xóa lớp học này?", "Xóa lớp học", JOptionPane.WARNING_MESSAGE);
        if (confirm == JOptionPane.YES_OPTION) {
            int index = tbl_Lop.getSelectedRow();
            LOP l = this.variables.getDatabase().getLopManager().getListLOP().get(index);
            if (cbb_khoa.getSelectedItem() != null) {
                String selectedKhoa = cbb_khoa.getSelectedItem().toString();
                cbb_form_khoa.setSelectedItem(selectedKhoa);
                String link = this.variables.getLink(this.variables.getDatabase().getCurrentServerName(), selectedKhoa);
                String query = "";
                if (link == null) {
                    query = "EXEC QLDSV_TC.DBO.SP_XOALOPHOC '" + l.getMaLop() + "'" ;
                } else {
                    query = "EXEC " + link + ".QLDSV_TC.DBO.SP_XOALOPHOC '" + l.getMaLop() + "'" ;
                }
                Statement stmt = this.variables.getDatabase().getStatement();
                try {
                    stmt.execute(query);
                    loadLopHoc();
                    putDataIntoTable();
                    JOptionPane.showMessageDialog(this, "Xóa lớp học thành công", "Xóa thành công", JOptionPane.INFORMATION_MESSAGE);
                } catch (SQLException ex) {
                    Logger.getLogger(pn_LopHoc.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(this, "Không thể xóa lớp học này", "Xóa thất bại", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }
    
    public void searchLopHoc() {
        String maLopHoc = txt_timKiem.getText();
        String query =  "EXEC QLDSV_TC.DBO.SP_TIMLOPTHEOMALOP '" + maLopHoc + "'";
        Statement stmt = this.variables.getDatabase().getStatement();
        try {
            ResultSet rs = stmt.executeQuery(query);
            this.variables.getDatabase().getLopManager().clearData();
            while(rs.next()) {
                LOP l = new LOP();
                l.setMaLop(rs.getString("MALOP"));
                l.setTenLop(rs.getString("TENLOP"));
                l.setKhoaHoc(rs.getString("KHOAHOC"));
                l.setMaKhoa(rs.getString("MAKHOA"));
                this.variables.getDatabase().getLopManager().insertLOP(l);
            }
            putDataIntoTable();
        } catch (SQLException ex) {
            Logger.getLogger(pn_LopHoc.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
