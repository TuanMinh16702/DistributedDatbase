    public void searchSinhVien() throws SQLException {
        SinhVien sv = new SinhVien();
        String maKhoa = "";
        String maLop = "";
        String mssv = txt_timKiem.getText();
        String query = "EXEC QLDSV_TC.DBO.SP_TimSV '" + mssv + "'";
        Statement statement = this.variables.getDatabase().getStatement();
        ResultSet rs = statement.executeQuery(query);
        while(rs.next()) {
            maKhoa = rs.getString("MAKHOA");
            maLop = rs.getString("MALOP");
            sv.setMaSV(rs.getString("MASV"));
            sv.setHo(rs.getString("HO"));
            sv.setTen(rs.getString("TEN"));
            sv.setPhai(rs.getInt("PHAI"));
            sv.setDiaChi(rs.getString("DIACHI"));
            sv.setNgaySinh(rs.getDate("NGAYSINH").toLocalDate());
            sv.setDaNghiHoc(rs.getInt("DANGHIHOC"));
            sv.setPassword(rs.getString("PASSWORD"));
            sv.setMaLop(maLop);
        }
        
        int cbb_khoa_form_item_count = cbb_khoa.getItemCount();
        for (int i = 0; i < cbb_khoa_form_item_count; i++) {
            if (cbb_khoa.getItemAt(i).contains(maKhoa.trim())) {
                cbb_khoa.setSelectedIndex(i);
            }
        }

        int cbb_lop_form_item_count = cbb_lop.getItemCount();
        for (int i = 0; i < cbb_lop_form_item_count; i++) {
            if (cbb_lop.getItemAt(i).contains(maLop.trim())) {
                cbb_lop.setSelectedIndex(i);
            }
        }
        
        SinhVienManager sinhVienManager = this.variables.getDatabase().getSinhVienManager();
        sinhVienManager.clearData();
        sinhVienManager.insertSinhVien(sv);
        putDataIntoTable();
    }
    
    public void putDataIntoTable() {
        DefaultTableModel studentModel = (DefaultTableModel) tblSinhvien.getModel();
        studentModel.setRowCount(0);
        List<SinhVien> myList = variables.getDatabase().getSinhVienManager().getListSinhVien();
        for (SinhVien item: myList){
            if (item.getDaNghiHoc() == this.variables.getCHUA_NGHI_HOC()){
                Object[] obj = new Object[]{
                    item.getMaSV(),
                    item.getHo(),
                    item.getTen(),
                    item.getPhai() == this.variables.getMALE() ? this.variables.getTEXT_MALE() : this.variables.getTEXT_FEMALE(),
                    item.getDiaChi(),
                    item.getNgaySinh()
                };
                studentModel.addRow(obj);
            }
        }
        sortTable();
    }
    
    public void putDataIntoForm(SinhVien sv){
        LOP lop = this.variables.getDatabase().getLopManager().searchByMaLOP(sv.getMaLop());
        if (lop != null) {
            String maKhoa = lop.getMaKhoa();
            int cbb_khoa_form_item_count = cbb_khoa_form.getItemCount();
            for (int i = 0; i < cbb_khoa_form_item_count; i++) {
                if (cbb_khoa_form.getItemAt(i).contains(maKhoa.trim())) {
                    cbb_khoa_form.setSelectedIndex(i);
                }
            }
            
            int cbb_lop_form_item_count = cbb_lop_form.getItemCount();
            for (int i = 0; i < cbb_lop_form_item_count; i++) {
                if (cbb_lop_form.getItemAt(i).contains(sv.getMaLop().trim())) {
                    cbb_lop_form.setSelectedIndex(i);
                }
            }
            
            txt_mssv.setText(sv.getMaSV());
            txt_ho.setText(sv.getHo());
            txt_ten.setText(sv.getTen());
            
            if (sv.getPhai() == this.variables.getMALE())
                rb_nam.setSelected(true);
            else 
                rb_nu.setSelected(true);
            
            txt_diaChi.setText(sv.getDiaChi());
            dc_ngaysinh.setDate(Date.from(sv.getNgaySinh().atStartOfDay(ZoneId.systemDefault()).toInstant()));
            
            if (sv.getDaNghiHoc()== this.variables.getCHUA_NGHI_HOC())
                rb_conHoc.setSelected(true);
            else 
                rb_daNghi.setSelected(true);
            
            pass_password.setText(sv.getPassword());
        }
        
    }
    
