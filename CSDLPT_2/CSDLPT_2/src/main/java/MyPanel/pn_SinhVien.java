    public void setUpKhoaComboBox() {
        cbb_khoa.removeAllItems();
        cbb_khoa_form.removeAllItems();
        Object[] servers = this.variables.getDatabase().getServers().keySet().toArray();
        
        if(this.variables.getUser().getDatabaseRole().equals("KHOA")) {
            cbb_khoa.addItem(this.variables.getDatabase().getCurrentServerName());
            cbb_khoa_form.addItem(this.variables.getDatabase().getCurrentServerName());
        } else {
            for(Object item: servers){
                if(!item.toString().equals(this.variables.getPHONG_KE_TOAN_NAME())){
                    cbb_khoa.addItem(item.toString());
                }
            }

            for(Object item: servers){
                if(!item.toString().equals(this.variables.getPHONG_KE_TOAN_NAME())){
                    cbb_khoa_form.addItem(item.toString());
                }
            }
        }
    }
    
    public void setUpLopComboBox(JComboBox cbb_khoa, JComboBox cbb_lop) throws SQLException {
        if (cbb_khoa.getSelectedItem() != null) {
            String selectedKhoa = cbb_khoa.getSelectedItem().toString();
            String link = this.variables.getLink(this.variables.getDatabase().getCurrentServerName(), selectedKhoa);
            String query = "";
            if (link == null) {
                query = "select * from QLDSV_TC.DBO.view_getAllLop";
                
            } else {
                query = "select * from " + link + ".QLDSV_TC.DBO.view_getAllLop";
            }
            Statement stmt = this.variables.getDatabase().getStatement();
            ResultSet rs = stmt.executeQuery(query);
            this.variables.getDatabase().getLopManager().clearData();
            while(rs.next()) {
                LOP l = new LOP();
                l.setMaLop(rs.getString("MALOP"));
                l.setTenLop(rs.getString("TENLOP"));
                l.setKhoaHoc(rs.getString("KHOAHOC"));
                l.setMaKhoa(rs.getString("MAKHOA"));
                this.variables.getDatabase().getLopManager().insertLOP(l);
            }
            cbb_lop.removeAllItems();
            for (LOP item: this.variables.getDatabase().getLopManager().getListLOP()){
                cbb_lop.addItem(item.getMaLop() + "- " + item.getTenLop());
            }
        }
    }
    
    public void loadSinhVien() throws SQLException {
        if (cbb_khoa.getSelectedItem() != null && cbb_lop.getSelectedItem() != null) {
            String selectedKhoa = cbb_khoa.getSelectedItem().toString();
            String selectedLop = cbb_lop.getSelectedItem().toString();
            String selectedMaLop = selectedLop.substring(0, selectedLop.lastIndexOf("-"));
            
            String link = this.variables.getLink(this.variables.getDatabase().getCurrentServerName(), selectedKhoa);
            String query = "";
            if (link == null) {
                query = "EXEC QLDSV_TC.DBO.SP_TIMSVTHEOMALOP '"+ selectedMaLop +"'";
            } else {
                query = "EXEC " + link + ".QLDSV_TC.DBO.SP_TIMSVTHEOMALOP'"+ selectedMaLop +"'";
            }
            
            Statement stmt = this.variables.getDatabase().getStatement();
            ResultSet rs = stmt.executeQuery(query);
            this.variables.getDatabase().getSinhVienManager().clearData();
            while(rs.next()) {
                if (rs.getInt("DANGHIHOC") == this.variables.getCHUA_NGHI_HOC()) {
                    SinhVien sv = new SinhVien();
                    sv.setMaSV(rs.getString("MASV"));
                    sv.setHo(rs.getString("HO"));
                    sv.setTen(rs.getString("TEN"));
                    sv.setPhai(rs.getInt("PHAI"));
                    sv.setDiaChi(rs.getString("DIACHI"));
                    sv.setNgaySinh(rs.getDate("NGAYSINH").toLocalDate());
                    sv.setMaLop(rs.getString("MALOP"));
                    sv.setDaNghiHoc(rs.getInt("DANGHIHOC"));
                    sv.setPassword(rs.getString("PASSWORD"));
                    this.variables.getDatabase().getSinhVienManager().insertSinhVien(sv);
                }
            }
            this.putDataIntoTable();
        }
    }
    
    public void insertSinhVien() {
        String lop = cbb_lop_form.getSelectedItem().toString();
        String maLop = lop.substring(0, lop.lastIndexOf(" - "));
        String mssv = txt_mssv.getText();
        String ho = txt_ho.getText();
        String ten = txt_ten.getText();
        int phai = rb_nam.isSelected() ? this.variables.getMALE() : this.variables.getFEMALE();
        String diaChi = txt_diaChi.getText();
        LocalDate ngaySinh = dc_ngaysinh.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        int daNghiHoc = rb_conHoc.isSelected() ? this.variables.getCHUA_NGHI_HOC() : this.variables.getDA_NGHI_HOC();
        String password = new String(pass_password.getPassword());
        
        String khoa = cbb_khoa_form.getSelectedItem().toString();
        String link = this.variables.getLink(this.variables.getDatabase().getCurrentServerName(), khoa);
        String query = "";
        if (link == null) {
            query = "EXEC QLDSV_TC.DBO.SP_THEMSV '" + maLop +"', '" + mssv + "', N'" + ho + "', N'" + ten + "', " + phai + ", N'" + diaChi + "', '" + ngaySinh.format(this.variables.getSQL_DATE_TIME_FORMATTER()) + "', " + daNghiHoc + ", '" + password + "'";
        } else {
            query = "EXEC " + link + ".QLDSV_TC.DBO.SP_THEMSV '" + maLop +"', '" + mssv + "', N'" + ho + "', N'" + ten + "', " + phai + ", N'" + diaChi + "', '" + ngaySinh.format(this.variables.getSQL_DATE_TIME_FORMATTER()) + "', " + daNghiHoc + ", '" + password + "'";
        }
        Statement stmt = this.variables.getDatabase().getStatement();
        
        try {
            stmt.execute(query);
            clearForm();
            
            if (!cbb_lop.getSelectedItem().toString().equals(cbb_lop_form.getSelectedItem().toString())){
                cbb_khoa.setSelectedItem(cbb_khoa_form.getSelectedItem());
                cbb_lop.setSelectedItem(cbb_lop_form.getSelectedItem());
            } else {
                loadSinhVien();
            }
            
            JOptionPane.showMessageDialog(this, "Thêm sinh viên thành công", "Thêm thành công", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(pn_SinhVien.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Thêm sinh viên thất bại", "Thêm thất bại", JOptionPane.ERROR_MESSAGE);
        }
    }
    
