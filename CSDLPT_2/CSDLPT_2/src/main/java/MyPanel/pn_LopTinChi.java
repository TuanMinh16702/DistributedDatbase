    public void putDataIntoTable() {
        DefaultTableModel classModel = (DefaultTableModel) tbl_LopTinChi.getModel();
        classModel.setRowCount(0);
        List<LOPTINCHI> myList = variables.getDatabase().getLopTinChiManager().getListLTC();
        for (LOPTINCHI item: myList){
            Object[] obj = new Object[]{
                item.getMaLTC(),
                item.getNienKhoa(),
                item.getHocKy(),
                item.getMaMH(),
                item.getNhom(),
                item.getMaGV(),
                item.getMaKhoa(),
                item.getSoSVTT(),
                item.getHuyLop(),
            };
            classModel.addRow(obj);
        }
        sortTable();
    }
    
    private void sortTable() {
        DefaultTableModel teacherModel = (DefaultTableModel) tbl_LopTinChi.getModel();
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(teacherModel);
        tbl_LopTinChi.setRowSorter(sorter);
    }
    
    public void clearAllDataFromTable() {
        DefaultTableModel subjectModel = (DefaultTableModel) tbl_LopTinChi.getModel();
        subjectModel.setRowCount(0);
    }
    

