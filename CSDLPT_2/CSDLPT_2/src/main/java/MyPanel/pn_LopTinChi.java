    public void loadLopTinChi() throws SQLException {
        if (cbb_khoa.getSelectedItem() != null && cbb_nienKhoa.getSelectedItem() != null && cbb_hocKy.getSelectedItem() != null) {
            String selectedKhoa = cbb_khoa.getSelectedItem().toString();
            String selectedNienKhoa = cbb_nienKhoa.getSelectedItem().toString();
            String selectedHocKy = cbb_hocKy.getSelectedItem().toString();
            String link = this.variables.getLink(this.variables.getDatabase().getCurrentServerName(), selectedKhoa);
            String query = "";
            if (link == null) {
                query = "EXEC QLDSV_TC.DBO.SP_GETLTC_NK_HK '" + selectedNienKhoa + "', '" + selectedHocKy + "'";
            } else {
                query = "EXEC " + link + ".QLDSV_TC.DBO.SP_GETLTC_NK_HK '" + selectedNienKhoa + "', '" + selectedHocKy + "'";
            }
            
            Statement stmt = this.variables.getDatabase().getStatement();
            ResultSet rs = stmt.executeQuery(query);
            this.variables.getDatabase().getLopTinChiManager().clearData();
            while(rs.next()) {
                LOPTINCHI ltc = new LOPTINCHI();
                ltc.setMaLTC(rs.getInt("MALTC"));
                ltc.setNienKhoa(rs.getString("NIENKHOA"));
                ltc.setHocKy(rs.getInt("HOCKY"));
                ltc.setMaMH(rs.getString("MAMH"));
                ltc.setNhom(rs.getInt("NHOM"));
                ltc.setMaGV(rs.getString("MAGV"));
                ltc.setMaKhoa(rs.getString("MAKHOA"));
                ltc.setSoSVTT(rs.getInt("SOSVTOITHIEU"));
                ltc.setHuyLop(rs.getInt("HUYLOP"));
                this.variables.getDatabase().getLopTinChiManager().insertLTC(ltc);
            }
            this.putDataIntoTable();
        }
    }
    
    public void insertLopTinChi() {
        String nienKhoa = txt_nienKhoa.getText();
        String hocKy = cbb_form_hocKy.getSelectedItem().toString();
        String maMonHoc = cbb_form_maMH.getSelectedItem().toString();
        String nhom = txt_nhom.getText();
        String maGiangVien = txt_maGiangVien.getText();
        String maKhoa = txt_maKhoa.getText();
        String soSVTT = txt_soSVTT.getText();
        
        String selectedKhoa = cbb_khoa.getSelectedItem().toString();
        String link = this.variables.getLink(this.variables.getDatabase().getCurrentServerName(), selectedKhoa);
        String query = "";
        if (link == null) {
            query = "EXEC QLDSV_TC.DBO.SP_THEMLTC '" + maKhoa + "', '" + nienKhoa + "', '" + hocKy + "', '" + maMonHoc + "', '" + nhom + "', '" + maGiangVien + "', '" + soSVTT + "'";
        } else {
            query = "EXEC " + link + ".QLDSV_TC.DBO.SP_THEMLTC '" + maKhoa + "', '" + nienKhoa + "', '" + hocKy + "', '" + maMonHoc + "', '" + nhom + "', '" + maGiangVien + "', '" + soSVTT + "'";
        }
        Statement stmt = this.variables.getDatabase().getStatement();
        try {
            stmt.execute(query);
            loadLopTinChi();
            JOptionPane.showMessageDialog(this, "Thêm lớp tín chỉ thành công", "Thành công", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(pn_LopTinChi.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Thêm lớp tín chỉ thất bại", "Thất bại", JOptionPane.ERROR_MESSAGE);
        }
    }
    
